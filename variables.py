#VARIABLES- it could be any value like string, integer, float etc
#           a variable is a container to store data values

#string variable
name = "Alice"
food = "Pizza"

print(f" I love {food}")
print(f"hello!, i am  {name}")

#integer variable
age = 25   

print(f"I am {age} years old")

#float variable
height = 5.7

print(f"My height is {height} feet")

#boolean variable

is_student = True
for_sale = False

if for_sale:
    print("the item is for sale")
else:
    print("item is sold")


if is_student:
    print("I am a student")
else:
    print("I am not a student")