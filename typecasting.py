#typecasting = converting one data type to another data type
#               int() , float() , str() , etc

#string to integer
num1 = "10"
num2 = "20"
total = int(num1) + int(num2)

print(f"the total is {total}")

#integer to string
num3 = 30
num4 = 40
total2 = str(num3) + str(num4)

print(f"the total is {total2}")

#float to integer
num5 = 5.7  
num6 = 4.3
total3 = int(num5) + int(num6)

print(f"the total is {total3}")

#integer to float
num7 = 7    
num8 = 8
total4 = float(num7) + float(num8)

print(f"the total is {total4}")

#string to boolean
str1 = "dhruv"
str2 = ""   
bool1 = bool(str1)
bool2 = bool(str2)

print(f"the value of bool1 is {bool1}")
print(f"the value of bool2 is {bool2}") 