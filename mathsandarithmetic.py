#maths annd arithmetic functions

x = 3.14
y = 2.0
z = 5

result1 = round(x)  # Rounds to the nearest integer
result2 = abs(-y)   # Absolute value
result3 = pow(z, 2)  # z raised to the power of 2
result4 = max(x, y, z)  # Maximum value

print(f"Rounded value of {x} is {result1}")
print(f"Absolute value of -{y} is {result2}")
print(f"{z} raised to the power of 2 is {result3}")
print(f"The maximum value among {x}, {y}, and {z} is {result4}")
